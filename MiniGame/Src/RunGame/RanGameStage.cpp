/*=============================================*/
/*　　　　　　　　　描画関数関数     　　　　　*/
/*=============================================*/
void RunGame::RanGameStage::Draw()
{

	for (int y = 0; y < DRAW_RANGE_HEIGHT; ++y) {
		for (int x = m_Draw_Range_Min; x < m_Draw_Range_Max; ++x) {
			int convet_pos_x = x - m_Draw_Range_Min;
			switch (m_Stage[y][x])
			{
			case 0:
				Drawer::GetInstance().SetDrawBuffer(convet_pos_x, y, "  ");
				break;

			case 1:
				Drawer::GetInstance().SetDrawBuffer(convet_pos_x, y, "■");
				break;

			case 2:
				Drawer::GetInstance().SetDrawBuffer(convet_pos_x, y, "〇");
				break;

			case 3:
				Drawer::GetInstance().SetDrawBuffer(convet_pos_x, y, "▽");
				break;

			default:
				break;
			}
		}
	}
	Drawer::GetInstance().SetDrawBuffer(0, DRAW_RANGE_HEIGHT+1, "Rキーでジャンプ！\n");

}


/*=================================================================*/
/*　プレイヤーの右の辺とブロックの左の辺との当たり判定を行う関数   */
/*=================================================================*/
bool RunGame::RanGameStage::HitPlyaerAndBlockLeftEdge(Vec2 pos_, __int8 width_, __int8 height_)
{
	// プレイヤーの右の辺を作成
	Vec2 player_right_edge[2] = {
		{pos_.m_X + width_, pos_.m_Y},
		{pos_.m_X + width_, pos_.m_Y + height_},
	};

	// ブロックに当たっているかを調べる
	for (int y = player_right_edge[0].m_Y; y < player_right_edge[1].m_Y; ++y) {
		for (int x = player_right_edge[0].m_X; x < player_right_edge[1].m_X; ++x) {
			if (g_Stage[y][x] == 1) {
				return true;
			}
		}
	}

	return false;
}


/*=================================================================*/
/*　プレイヤーの下の辺とブロックの上の辺との当たり判定を行う関数   */
/*=================================================================*/
bool RunGame::RanGameStage::HitPlayerAndBlockTopEdge(Vec2 pos_, __int8 width_, __int8 height_, float* contactPos_)
{
	// プレイヤーの下の辺を作成
	Vec2 player_bottom_edge[2] = {
		{pos_.m_X, pos_.m_Y + height_},
		{pos_.m_X + width_, pos_.m_Y + height_},
	};

	// プレイヤーの下の辺にブロックが当たってるかを調べる
	for (int y = player_bottom_edge[0].m_Y; y < player_bottom_edge[1].m_Y; ++y) {
		for (int x = player_bottom_edge[0].m_X; x < player_bottom_edge[1].m_X; ++x) {
			if (g_Stage[y][x] == 1) {
				*contactPos_ = static_cast<float>(y);
				return true;
			}
		}
	}

	return false;
}


/*=============================================*/
/*　　　　ステージにセットする関数     　　　　*/
/*=============================================*/
void RunGame::RanGameStage::SetStage(Vec2 pos_)
{
	// ステージをクリアする
	memcpy(&m_Stage, g_Stage, sizeof(g_Stage));

	// ステージに書き込む
	m_Stage[static_cast<int>(pos_.m_Y)][static_cast<int>(pos_.m_X)]   = 2;
	m_Stage[static_cast<int>(pos_.m_Y)+1][static_cast<int>(pos_.m_X)] = 3;

	m_Draw_Range_Min = static_cast<int>(pos_.m_X - 1);
	m_Draw_Range_Max = static_cast<int>(pos_.m_X + DRAW_RANGE_WIDTH);
}
